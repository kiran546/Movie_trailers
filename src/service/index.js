const queryUrl    =`${url}/search/movie`;


// https://api.themoviedb.org/3/search/movie?api_key=a4999a28333d1147dbac0d104526337a&query=Jack+Reacher

export const fetchQuery = async (querystring) => {
    console.log(querystring,"queryyyy")
    try {
        const { data } = await axios.get(queryUrl, {
            
            params: {
                api_key: apiKey,
                query: querystring.trim().split(" ").join("+")
               
            }
           
        })

        console.log(data['results'],"data")
        const posterUrl = 'https://image.tmdb.org/t/p/original/';
        const modifiedData = data['results'].map((m) => ({
            id: m['id'],
            backPoster: posterUrl + m['backdrop_path'],
            popularity: m['popularith'],
            title: m['title'],
            poster: posterUrl + m['poster_path'],
            overview: m['overview'],
            rating: m['vote_average'],
        }))

        return modifiedData;
    } catch (error) { }
}





